namespace Topten.RichTextKit
{
    /// <summary>
    /// Describes the underline style for a run of text
    /// </summary>
    public enum FontWidthStyle

    {
        /// <summary>
        /// Normal width.
        /// </summary>
        Normal = 5,

        /// <summary>
        /// Condensed width.
        /// </summary>
        Condensed = 3,

        /// <summary>
        /// Expanded width.
        /// </summary>
        Expanded = 7, 	

        /// <summary>
        /// Extra condensed width.
        /// </summary>
        ExtraCondensed = 2, 	
        
        /// <summary>
        /// Extra expanded width.
        /// </summary>
        ExtraExpanded =	8, 	

        /// <summary>
        /// Semi condesnsed width.
        /// </summary>
        SemiCondensed = 4, 	

        /// <summary>
        /// Semi expanded width.
        /// </summary>
        SemiExpanded = 6, 	

        /// <summary>
        /// Ultra condensed width.
        /// </summary>
        UltraCondensed = 1, 	

        /// <summary>
        /// Ultra expanded width
        /// </summary>
        UltraExpanded =	9,
    }
}
